-> from 1.5 version onwards primitive place Wrapper and wrapper place primitive can give
because of compiler take care of this with auto conversion called  autoBoxing and autoUnboxing

AutoBoxing :
------------

int to Integer -> primitive to Wrapper called AutoBoxing
Integer I = 10;
Integer I = Integer.ValueOf(10) -> internally





AutoUnboxing :
--------------
Integer to int -> Integer to int

Integer I = new Integer(10)
int i = I
int i = I.intValue();

