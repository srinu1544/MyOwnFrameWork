/* https://coil-kt.github.io/coil/

An image loading library for Android backed by Kotlin Coroutines. Coil is:

Fast:
Coil performs a number of optimizations including memory and disk caching, downsampling
the image in memory, automatically pausing/cancelling requests, and more.

Lightweight:
Coil adds ~2000 methods to your APK (for apps that already use OkHttp and Coroutines),
which is comparable to Picasso and significantly less than Glide and Fresco.

Easy to use:
Coil's API leverages Kotlin's language features for simplicity and minimal boilerplate.

Modern:
Coil is Kotlin-first and uses modern libraries including Coroutines, OkHttp, Okio,
and AndroidX Lifecycles.




*/