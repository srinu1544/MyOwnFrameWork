package com.example.myownframework.Kotlin_Android.Differences

/*Android Studio projects have two build.gradle files:

Project-level Gradle :
---------------
This file defines global settings and configurations for the entire project.
It also implements dependencies and plugins at the top level.

Application-level Gradle :

This file contains build settings specific to the application module.

The project-level build.gradle file allows you to add configuration options that are common
 to all application modules in the project.

You can merge multiple build.gradle files into one to reduce redundancy and make it easier to
manage everything in one file*/