/*Git is a free and open-source distributed version control system designed to
handle everything from small to very large projects with speed and efficiency.

Here are some of the main topics you should learn about Git:

Version control:
Git keeps track of changes to your files and allows you to revert to previous versions if needed.

Branching:
Git allows you to create branches of your codebase, so you can experiment with new features
without affecting the main codebase.

Merging:
Git allows you to merge changes from different branches back into the main codebase.

Collaboration:
Git makes it easy to collaborate with other developers on the same project.

Here are some of the basic Git commands:
git init: Initializes a new Git repository.
git add: Adds files to the staging area.
git commit: Commits changes to the repository.
git push: Pushes changes to a remote repository.
git pull: Pulls changes from a remote repository.



1. Setting Up:

git init: Initializes a new Git repository in the current directory.
git clone [URL]: Creates a local copy of a remote repository.
git config: Sets various configuration options for Git, like your name and email.


2. Working with Changes:

git add [file]: Adds a file to the staging area for the next commit.
git add -A: Adds all modified and untracked files to the staging area.
git rm [file]: Removes a file from the working directory and staging area.
git commit -m "[message]": Creates a commit with a descriptive message.
git diff: Shows the difference between the working directory, staging area, or commits.
git status: Shows the current state of the working directory and staging area.


3. Branching and Merging:

git branch: Lists, creates, or deletes branches.
git checkout [branch]: Switches to a different branch.
git merge [branch]: Merges changes from another branch into the current branch.


4. Inspecting History:

git log: Shows the commit history of the repository.
git tag: Creates, lists, or deletes tags.
git blame: Shows which commit introduced a specific line of code.


5. Undoing Changes:

git reset: Unstages or removes changes from the working directory or staging area.
git revert: Creates a new commit that reverses the changes introduced in another commit.
git stash: Temporarily saves uncommitted changes for later use.


6. Additional Commands:

git remote: Adds, removes, or fetches from remote repositories.
git push: Uploads local commits to a remote repository.
git pull: Downloads changes from a remote repository and merges them into the local branch.
.gitignore: Specifies files or patterns to be ignored by Git.



*/