/*

A foreign key (FK) is a column or group of columns that establishes and enforces a
connection between data in two tables. Foreign keys are used to control the data that
can be stored in the foreign key table


Foreign keys are fields in one table that reference the primary key in another table.
For example, the CustomerID column in the Orders table is a foreign key that refers
to the CustomerID column in the Customers table.

Foreign keys enforce referential integrity. This means that if column value A refers to
column value B, then column value B must exist.

Foreign keys streamline data sets. They allow database administrators to make data available
to different tables without creating redundant data sets */
